public class Ocean {

    private double o;
    private double c;
    private double e;
    private double a;
    private double n;

    public Ocean() {

    }

    public double getC() {
        return c;
    }

    public void setC(double c) {
        this.c = c;
    }

    public double getE() {
        return e;
    }

    public void setE(double e) {
        this.e = e;
    }

    public double getA() {
        return a;
    }

    public void setA(double a) {
        this.a = a;
    }

    public double getN() {
        return n;
    }

    public void setN(double n) {
        this.n = n;
    }

    public Ocean(double o, double c, double e, double a, double n) {
        this.o = o;
        this.c = c;
        this.e = e;
        this.a = a;
        this.n = n;
    }

    public double getO() {
        return o;
    }

    public void setO(double o) {
        this.o = o;
    }
}
